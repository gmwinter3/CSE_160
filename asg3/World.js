function initShapes() {
  // iterate over every grid cell
  for (let x = 0; x < world.length; x++) {
      for (let z = 0; z < world[x].length; z++) {
          const height = world[x][z];

          // for each level 0…height−1, create one cube
          for (let y = 0; y < height; y++) {
              const cube = new Cube();  // Create new cube each time
              cube.matrix.translate(x, y, z);
              cube.renderfaster();
          }
      }
  }
}

  

let lastTime = performance.now();
let frames = 0;
let fpsTime = 0;

function tick() {
  const now = performance.now();
  const dt = (now - lastTime) / 1000;
  lastTime = now;

  // Log to console
  console.log(`Frame time: ${(dt * 1000).toFixed(2)} ms`);

  // Update HTML
  frames++;
  fpsTime += dt;
  if (fpsTime >= 1.0) {
    const fps = Math.round(frames / fpsTime);
    const fpsDisplay = document.getElementById('numdot');
    if (fpsDisplay) {
      fpsDisplay.innerText = `FPS: ${fps}`;
    } else {
      console.warn("Couldn't find element with ID 'numdot'");
    }
    frames = 0;
    fpsTime = 0;
  }

  camera.update(dt);
  renderAllShapes();
  requestAnimationFrame(tick);
}

